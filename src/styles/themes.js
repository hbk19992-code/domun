function buildTheme({
  label,
  description,
  bg,
  panel,
  panelSoft,
  elevated,
  input,
  chip,
  button,
  border,
  borderStrong,
  text,
  textMuted,
  textDim,
  accent,
  accentStrong,
  accentText,
  onAccent,
  accentGradient,
  accentSoft,
  codeBg,
  success,
  successText,
  successSoft,
  warning,
  warningText,
  warningSoft,
  danger,
  dangerText,
  dangerSoft,
  info,
  infoText,
  infoSoft,
  star,
  starMuted,
  figureBg,
  figureText,
  figureBorder,
  headerBg,
  overlay,
  shadow,
}) {
  return {
    label,
    description,
    vars: {
      '--theme-bg': bg,
      '--theme-panel': panel,
      '--theme-panelSoft': panelSoft,
      '--theme-elevated': elevated,
      '--theme-input': input,
      '--theme-chip': chip,
      '--theme-button': button,
      '--theme-border': border,
      '--theme-borderStrong': borderStrong,
      '--theme-text': text,
      '--theme-textMuted': textMuted,
      '--theme-textDim': textDim,
      '--theme-accent': accent,
      '--theme-accentStrong': accentStrong,
      '--theme-accentText': accentText,
      '--theme-onAccent': onAccent,
      '--theme-accentGradient': accentGradient,
      '--theme-accentSoft': accentSoft,
      '--theme-codeBg': codeBg,
      '--theme-success': success,
      '--theme-successText': successText,
      '--theme-successSoft': successSoft,
      '--theme-warning': warning,
      '--theme-warningText': warningText,
      '--theme-warningSoft': warningSoft,
      '--theme-danger': danger,
      '--theme-dangerText': dangerText,
      '--theme-dangerSoft': dangerSoft,
      '--theme-info': info,
      '--theme-infoText': infoText,
      '--theme-infoSoft': infoSoft,
      '--theme-star': star,
      '--theme-starMuted': starMuted,
      '--theme-figureBg': figureBg,
      '--theme-figureText': figureText,
      '--theme-figureBorder': figureBorder,
      '--theme-headerBg': headerBg,
      '--theme-overlay': overlay,
      '--theme-shadow': shadow,
    },
  }
}

